package com.cheyooh.service.sdk.idata.cmccmm;

import org.nuxeo.common.xmap.annotation.XNode;
import org.nuxeo.common.xmap.annotation.XObject;

@XObject("PaycodeStatusResponse")
public class XmlCmccmmPayCodeStatusResponse {
	@XNode(value = "TransactionId")
	private String TransactionId = "";
	
	@XNode(value = "RspCode")
	private String RspCode = "";
	
	@XNode(value = "RspDesc")
	private String RspDesc = "";

	public String getTransactionId() {
		return TransactionId;
	}

	public void setTransactionId(String transactionId) {
		TransactionId = transactionId;
	}

	public String getRspCode() {
		return RspCode;
	}

	public void setRspCode(String rspCode) {
		RspCode = rspCode;
	}

	public String getRspDesc() {
		return RspDesc;
	}

	public void setRspDesc(String rspDesc) {
		RspDesc = rspDesc;
	}

	@Override
	public String toString() {
		return "XmlCmccmmPayCodeStatusResponse [TransactionId=" + TransactionId
				+ ", RspCode=" + RspCode + ", RspDesc=" + RspDesc + "]";
	}
	
	
}
