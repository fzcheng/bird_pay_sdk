package com.cheyooh.service.sdk.idata.cmccmm;

import org.nuxeo.common.xmap.annotation.XNode;
import org.nuxeo.common.xmap.annotation.XObject;

@XObject
public class XmlCmccmmPayCodeReqInfoSchema {

	@XNode(value = "PayCodeName")
	private String PayCodeName = "";
	
	@XNode(value = "PayCodeType")
	private String PayCodeType = "";
	
	@XNode(value = "Days")
	private String Days = "";
	
	@XNode(value = "Fee")
	private String Fee = "";
	
	@XNode(value = "AuthFlag")
	private String AuthFlag = "";
	
	@XNode(value = "AuthURL")
	private String AuthURL = "";
	
	@XNode(value = "SyncsubsURL")
	private String SyncsubsURL = "";
	
	@XNode(value = "PayCodeIndex")
	private String PayCodeIndex = "";

	public String getPayCodeName() {
		return PayCodeName;
	}

	public void setPayCodeName(String payCodeName) {
		PayCodeName = payCodeName;
	}

	public String getPayCodeType() {
		return PayCodeType;
	}

	public void setPayCodeType(String payCodeType) {
		PayCodeType = payCodeType;
	}

	public String getDays() {
		return Days;
	}

	public void setDays(String days) {
		Days = days;
	}

	public String getFee() {
		return Fee;
	}

	public void setFee(String fee) {
		Fee = fee;
	}

	public String getAuthFlag() {
		return AuthFlag;
	}

	public void setAuthFlag(String authFlag) {
		AuthFlag = authFlag;
	}

	public String getAuthURL() {
		return AuthURL;
	}

	public void setAuthURL(String authURL) {
		AuthURL = authURL;
	}

	public String getSyncsubsURL() {
		return SyncsubsURL;
	}

	public void setSyncsubsURL(String syncsubsURL) {
		SyncsubsURL = syncsubsURL;
	}

	public String getPayCodeIndex() {
		return PayCodeIndex;
	}

	public void setPayCodeIndex(String payCodeIndex) {
		PayCodeIndex = payCodeIndex;
	}

	@Override
	public String toString() {
		return "XmlCmccmmPayCodeReqInfoSchema [PayCodeName=" + PayCodeName
				+ ", PayCodeType=" + PayCodeType + ", Days=" + Days + ", Fee="
				+ Fee + ", AuthFlag=" + AuthFlag + ", AuthURL=" + AuthURL
				+ ", SyncsubsURL=" + SyncsubsURL + ", PayCodeIndex="
				+ PayCodeIndex + "]";
	}
	
}
