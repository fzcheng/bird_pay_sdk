package com.cheyooh.service.sdk.idata.cmccmm;

import org.nuxeo.common.xmap.annotation.XNode;
import org.nuxeo.common.xmap.annotation.XObject;

@XObject
public class XmlCmccmmProgramResultSchema {
	@XNode(value = "ProgramID")
	private String ProgramID = "";
	
	@XNode(value = "Type")
	private String Type = "";
	
	@XNode(value = "Status")
	private String Status = "";
	
	@XNode(value = "Program")
	private String Program = "";
	
	@XNode(value = "Desc")
	private String Desc = "";
	
	@XNode(value = "PackageName")
	private String PackageName = "";
	
	@XNode(value = "VersionCode")
	private String VersionCode = "";
	
	@XNode(value = "VersionName")
	private String VersionName = "";
	
	@XNode(value = "CopyRightXML")
	private String CopyRightXML = "";
	
	@XNode(value = "MMIAPXML")
	private String MMIAPXML = "";

	public String getProgramID() {
		return ProgramID;
	}

	public void setProgramID(String programID) {
		ProgramID = programID;
	}

	public String getType() {
		return Type;
	}

	public void setType(String type) {
		Type = type;
	}

	public String getStatus() {
		return Status;
	}

	public void setStatus(String status) {
		Status = status;
	}

	public String getProgram() {
		return Program;
	}

	public void setProgram(String program) {
		Program = program;
	}

	public String getDesc() {
		return Desc;
	}

	public void setDesc(String desc) {
		Desc = desc;
	}

	public String getPackageName() {
		return PackageName;
	}

	public void setPackageName(String packageName) {
		PackageName = packageName;
	}

	public String getVersionCode() {
		return VersionCode;
	}

	public void setVersionCode(String versionCode) {
		VersionCode = versionCode;
	}

	public String getVersionName() {
		return VersionName;
	}

	public void setVersionName(String versionName) {
		VersionName = versionName;
	}

	public String getCopyRightXML() {
		return CopyRightXML;
	}

	public void setCopyRightXML(String copyRightXML) {
		CopyRightXML = copyRightXML;
	}

	public String getMMIAPXML() {
		return MMIAPXML;
	}

	public void setMMIAPXML(String mMIAPXML) {
		MMIAPXML = mMIAPXML;
	}

	@Override
	public String toString() {
		return "XmlCmccmmProgramResultSchema [ProgramID=" + ProgramID
				+ ", Type=" + Type + ", Status=" + Status + ", Program="
				+ Program + ", Desc=" + Desc + ", PackageName=" + PackageName
				+ ", VersionCode=" + VersionCode + ", VersionName="
				+ VersionName + ", CopyRightXML=" + CopyRightXML
				+ ", MMIAPXML=" + MMIAPXML + "]";
	}
	
}
