package com.cheyooh.service.sdk.idata.cmccmm;

import org.nuxeo.common.xmap.annotation.XNode;
import org.nuxeo.common.xmap.annotation.XObject;

@XObject("AppQueryRequest")
public class XmlCmccmmAppQueryRequest {
	@XNode(value = "TransactionId")
	private String TransactionId = "";
	
	@XNode(value = "APCode")
	private String APCode = "";
	
	@XNode(value = "APPID")
	private String APPID = "";

	public String getTransactionId() {
		return TransactionId;
	}

	public void setTransactionId(String transactionId) {
		TransactionId = transactionId;
	}

	public String getAPCode() {
		return APCode;
	}

	public void setAPCode(String aPCode) {
		APCode = aPCode;
	}

	public String getAPPID() {
		return APPID;
	}

	public void setAPPID(String aPPID) {
		APPID = aPPID;
	}

	@Override
	public String toString() {
		return "XmlCmccmmAppQueryRequest [TransactionId=" + TransactionId
				+ ", APCode=" + APCode + ", APPID=" + APPID + "]";
	}
	
}
